import Movie from "../Presentation /Movie";

const ListOfMovies = ({ movies, onSelectedMovie }) => {
  return (
    <ul className="list list-movies">
      {movies?.map((movie) => (
        <Movie
          movie={movie}
          key={movie.imdbID}
          onSelectedMovie={onSelectedMovie}
        />
      ))}
    </ul>
  );
};

export default ListOfMovies;
